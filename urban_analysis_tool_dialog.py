import os
from qgis.PyQt import uic
from qgis.PyQt import QtWidgets
from qgis.PyQt.QtWidgets import QDialog, QMessageBox
from collections import defaultdict

from qgis.core import QgsMapLayerProxyModel, QgsProject, QgsVectorLayer, QgsFeature, QgsGeometry, QgsField, QgsSingleSymbolRenderer, QgsSymbol, QgsRendererCategory, QgsCategorizedSymbolRenderer, QgsHeatmapRenderer, QgsStyle, QgsCoordinateTransform, QgsCoordinateReferenceSystem, QgsMessageLog, Qgis
